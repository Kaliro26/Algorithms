// Q7. Write a program to accept two integers from the user, perform all arithmetic operations on it, and 
// display the result
#include <iostream>

using namespace std;

int main()
{
  int a;
  cout<<"enter the value of a : ";
  cin>>a;
  int b;
  cout<<"enter the value of b : ";
  cin>>b;
  cout<<"sum of a and b is ";
  cout<<a+b;
  cout<<endl;
  cout<<"multiply of a and b is ";
  cout<<a*b;
  cout<<endl;
  cout<<"subtraction of a and b is ";
  cout<<a-b;
  cout<<endl;
  cout<<"divison of a and b is ";
  cout<<a/b;
  cout<<endl;
  cout<<"modulo of a and b is ";
  cout<<a%b;
  cout<<endl;
    
}
